     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store()
    {
        $rules = [
            'name' => 'required|min:6',
            'email' => 'required|email',
            'active' => 'required',
            'company_id' => 'required',
        ];

        // Validate the user
        $data = request()->validate($rules);

        Customer::create($data);
        
        return redirect('customers');
    }

